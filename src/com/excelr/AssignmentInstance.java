package com.excelr;

public class AssignmentInstance {
	int x = 100;
	byte b = 101;
	short s = 102;
	public static void main(String[] args) {
		
		AssignmentInstance AI = new AssignmentInstance();
		int res1 = AI.x;
		System.out.println(res1);
		byte res2 = AI.b;
		System.out.println(res2);
		short res3 = AI.s;
		System.out.println(res3);

	}

}
